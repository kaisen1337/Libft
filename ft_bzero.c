#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*a;
	size_t			i;

	if (!s)
		return ;
	a = s;
	i = 0;
	while (i < n)
	{
		a[i] = 0;
		i++;
	}
}
